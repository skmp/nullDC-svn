////////////////////////////////////////////////////////////////////////////////////////
// Init()
////////////////////////////////////////////////////////////////////////////////////////
template<typename T, int CHANNELBUFFERSIZE>
TError CAICADSP<T,CHANNELBUFFERSIZE>::Init()
{
  TError Error = RET_OK;

  End();

  m_bInit = true;

  return Error;
}


////////////////////////////////////////////////////////////////////////////////////////
// End()
////////////////////////////////////////////////////////////////////////////////////////
template<typename T, int CHANNELBUFFERSIZE>
void CAICADSP<T,CHANNELBUFFERSIZE>::End()
{
  if ( m_bInit )
  {
    m_bInit = false;
  }
}