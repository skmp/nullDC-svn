////////////////////////////////////////////////////////////////////////////////////////
// Init()
////////////////////////////////////////////////////////////////////////////////////////
template<typename T, int BUFFERSIZE>
TError CAICADSPChannel<T,BUFFERSIZE>::Init()
{
  TError Error = RET_OK;

  End();

  m_bInit = true;

  return Error;
}


////////////////////////////////////////////////////////////////////////////////////////
// End()
////////////////////////////////////////////////////////////////////////////////////////
template<typename T, int BUFFERSIZE>
void CAICADSPChannel<T,BUFFERSIZE>::End()
{
  if ( m_bInit )
  {
    m_bInit = false;
  }
}